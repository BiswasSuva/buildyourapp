import AntDesign from './glyphmaps/AntDesign.json'
import Entypo from './glyphmaps/Entypo.json'
import EvilIcons from './glyphmaps/EvilIcons.json'
import Feather from './glyphmaps/Feather.json'
import FontAwesome from './glyphmaps/FontAwesome.json'
import FontAwesome5Free from './glyphmaps/FontAwesome5Free.json'
import FontAwesome5Free_meta from './glyphmaps/FontAwesome5Free_meta.json'
import FontAwesome5Pro from './glyphmaps/FontAwesome5Pro.json'
import FontAwesome5Pro_meta from './glyphmaps/FontAwesome5Pro_meta.json'
import Fontisto from './glyphmaps/Fontisto.json'
import Foundation from './glyphmaps/Foundation.json'
import Ionicons from './glyphmaps/Ionicons.json'
import MaterialCommunityIcons from './glyphmaps/MaterialCommunityIcons.json'
import MaterialIcons from './glyphmaps/MaterialIcons.json'
import Octicons from './glyphmaps/Octicons.json'
import SimpleLineIcons from './glyphmaps/SimpleLineIcons.json'
import Zocial from './glyphmaps/Zocial.json'


function FindIcon( fontName ) {




    if (fontName == 'AntDesign') {
        return AntDesign
    }
    if (fontName == 'Entypo') {
        return Entypo
    }
    if (fontName == 'EvilIcons') {
        return EvilIcons
    }
    if (fontName == 'Feather') {
        return Feather
    }
    if (fontName == 'FontAwesome') {
        return FontAwesome
    }
    if (fontName == 'FontAwesome5Free') {
        return FontAwesome5Free
    }
    if (fontName == 'FontAwesome5Free_meta') {
        return FontAwesome5Free_meta
    }
    if (fontName == 'FontAwesome5Pro') {
        return FontAwesome5Pro
    }
    if (fontName == 'FontAwesome5Pro_meta') {
        return FontAwesome5Pro_meta
    }
    if (fontName == 'Fontisto') {
        return Fontisto
    }
    if (fontName == 'Foundation') {
        return Foundation
    }
    if (fontName == 'Ionicons') {
        return Ionicons
    }
    if (fontName == 'MaterialCommunityIcons') {
        return MaterialCommunityIcons
    }
    if (fontName == 'MaterialIcons') {
        return MaterialIcons
    }
    if (fontName == 'Octicons') {
        return Octicons
    }
    if (fontName == 'SimpleLineIcons') {
        return SimpleLineIcons
    }
    if (fontName == 'Zocial') {
        return Zocial
    }
}

export default FindIcon